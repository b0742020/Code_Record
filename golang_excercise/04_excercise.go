package main

import "fmt"

func main() {
	var a string = "Hello, World!"
	var b int = 10
	var c float64 = 3.14159265359555555555

	fmt.Printf("a is of type %T and its value is %v\n", a, a)
	fmt.Printf("b is of type %T and its value is %v\n", b, b)
	fmt.Printf("c is of type %T and its value is %v\n", c, c)
}
